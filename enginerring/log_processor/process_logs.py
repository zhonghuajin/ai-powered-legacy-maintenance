def main():
    parser = argparse.ArgumentParser(
        description="Processes the log files generated by tests and restores the source code."
    )
    parser.add_argument(
        "--language",
        default="java",
        help="Target programming language (default: java)"
    )
    parser.add_argument(
        "--target-folders-file",
        default=".\\target-folders.txt",
        help="File containing target folder paths (one per line). Lines starting with '#' and empty lines are ignored. Default: .\\target-folders.txt"
    )
    parser.add_argument(
        "--target-folders",
        nargs="+",
        help="One or more target folder paths. If specified, takes precedence over --target-folders-file."
    )
    parser.add_argument(
        "--log-file",
        required=True,
        help="Path to the deduplicated instrumentation log file"
    )
    parser.add_argument(
        "--block-line-mapping-file",
        required=True,
        help="Path to the comment mapping file"
    )
    parser.add_argument(
        "--block-signature-file",
        default=None,
        help="Path to the block-signature mapping file. If provided, final-output-calltree.md will be automatically sorted."
    )
    parser.add_argument(
        "--pruned-folder",
        default="pruned\\",
        help="Path to the pruned output folder (default: pruned\\)"
    )
    parser.add_argument(
        "--events-file",
        required=True,
        help="Path to the structured events output file"
    )
    parser.add_argument(
        "--event-dictionary-file",
        default="event_dictionary.txt",
        help="Path to the event dictionary output file (default: event_dictionary.txt)"
    )
    parser.add_argument(
        "--block-pruner-jar",
        default=None,
        help="Path to the Block Pruner jar (defaults to Javatarget path)"
    )
    parser.add_argument(
        "--data-structuring-jar",
        default=None,
        help="Path to the Data Structuring jar (defaults to Javatarget path)"
    )
    parser.add_argument(
        "--base-reference-dir",
        default=None,
        help="Base directory to preserve relative directory structures"
    )

    args = parser.parse_args()

    try:
        process_logs(
            language=args.language,
            target_folders_file=args.target_folders_file,
            target_folders=args.target_folders,
            log_file=args.log_file,
            block_line_mapping_file=args.block_line_mapping_file,
            block_signature_file=args.block_signature_file,
            pruned_folder=args.pruned_folder,
            events_file=args.events_file,
            event_dictionary_file=args.event_dictionary_file,
            block_pruner_jar=args.block_pruner_jar,
            data_structuring_jar=args.data_structuring_jar,
            base_reference_dir=args.base_reference_dir,
        )
    except Exception as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)

if __name__ == "__main__":
    main()