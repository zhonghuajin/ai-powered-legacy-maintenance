def generate_prompt(cli_file_path=None):
    """
    生成缺陷定位 Prompt 的对外接口。
    """
    print("="*50)
    print("🕵️  AI 缺陷定位 Prompt 自动生成器")
    print("="*50)
    print("请根据提示输入所需信息（直接按回车跳过选填项）\n")

    # [修改] 0. 选择分析模式（硬编码为调用树优先）
    selected_template = SIMPLE_PROMPT_TEMPLATE
    mode = "1"
    print("✅ [自动配置] 分析模式已自动设置为：[1] 调用树优先（省略并发部分）。\n")

    # 1. 收集缺陷症状
    bug_symptom = input(
        "🐞 1. 请描述【可观测症状】（例如：事件驱动聚合测试错误地输出了全零数组，而不是预期的计算值，因为程序在后台任务处理完成前就读取了结果）：\n> ").strip()
    if not bug_symptom:
        bug_symptom = "[未提供具体症状。请分析追踪数据中明显的逻辑错误或异常。]"

    # 2. 收集补充说明
    additional_info = input(
        "\n💬 2. 请输入【补充说明】（选填，例如：怀疑 SyncTest.sharedData 缺少 volatile 关键字）：\n> ").strip()
    if not additional_info:
        additional_info = "无特殊补充说明。请遵循事实轨迹进行分析。"

    # 3. 读取追踪数据文件
    trace_data = ""
    
    while True:
        if cli_file_path:
            file_path = cli_file_path
            print(f"\n📁 3. 使用参数传入的调用树文件：{file_path}")
            # 重置变量，如果失败则回退到手动输入
            cli_file_path = None
        else:
            # 特定模式的文件提示（由于模式始终为 "1"，因此始终使用调用树文件提示）
            if mode == "2":
                file_hint = "包含并发信息的调用树文件 (例如: ../../final-output-combined.md)"
            else:
                file_hint = "调用树文件 (例如: ../../final-output-calltree.md)"

            file_path = input(
                f"\n📁 3. 请输入【{file_hint}】的路径：\n> ").strip()
            # 移除可能存在的引号（从终端拖拽文件时常见）
            file_path = file_path.strip('\'"')

        if not file_path:
            print("❌ 文件路径不能为空，请重新输入！")
            continue

        if not os.path.exists(file_path):
            print(
                f"❌ 找不到文件：{file_path}。请检查路径是否正确！")
            continue

        try:
            with open(file_path, 'r', encoding='utf-8') as f:
                trace_data = f.read()
            print("✅ 成功加载运行时追踪数据！")
            break
        except Exception as e:
            print(f"❌ 读取文件失败：{e}")
            continue

    # 4. 组装最终 Prompt
    final_prompt = selected_template.format(
        bug_symptom=bug_symptom,
        additional_info=additional_info,
        trace_data=trace_data
    )

    # 5. 写入文件
    output_filename = "AI_Task_Prompt.md"
    try:
        with open(output_filename, 'w', encoding='utf-8') as f:
            f.write(final_prompt)
        print("\n" + "="*50)
        print(
            f"🎉 成功！完整的 Prompt 已生成并保存在当前目录下：{output_filename}")
        print("👉 你现在可以直接打开此文件，复制所有内容并发送给 AI！")
        print("="*50)
    except Exception as e:
        print(f"\n❌ 保存文件失败：{e}")


def main():
    cli_file_path = sys.argv[1] if len(sys.argv) > 1 else None
    generate_prompt(cli_file_path)


if __name__ == "__main__":
    try:
        main()
    except KeyboardInterrupt:
        print("\n\n🛑 用户取消了操作。")
